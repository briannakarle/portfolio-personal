            <section class="projectDetails">
                <h4>Project Details</h4>
                <div class="projectText">
                    <p>For this website, my client wanted a single page with very little information about the business. The “K.I.S.S.” in Kiss Cafe stand for “Keep It Simple Stupid”.  She only wanted there to be location, the menu, the hours, and a link to facebook. It was a challenge to make a site this small!  I also had to make it using the new branding of the company. </p> 
                     <p>One really fun thing that the business has been doing is putting A-frame sidewalk signs near the cafe.  On these, they put a bunch of neat little sayings.  The letter is done with stick on vinyl so that they can constantly change the sayings.  The client was very excited about these signs, so I decided to build the website to look like them.  Using jQuery, I created the site so that each time the page is loaded, it shows a different saying from the signs. </p> 
                    <p>Also to keep the website simple, I simply hid the menu and the map on the page.  When the Menu or Map buttons are pushed, the corresponding item appears.  In addition to this, when the menu shows up, you can click and enlarge it.  This makes the menu far easier to read, especially on mobile.  </p>
